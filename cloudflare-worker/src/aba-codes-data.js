// Fuzzy ABA code matcher - handles subspecies and bracketed content
export function getAbaCode(speciesName) {
  if (!speciesName) return null;
  
  // Try exact match first
  let code = ABA_CODES[speciesName];
  if (code !== undefined) return code;
  
  // Try without bracketed parts
  const cleanName = speciesName.replace(/\s*\([^)]*\)/g, "", "").trim();
  if (cleanName !== speciesName) {
    code = ABA_CODES[cleanName];
    if (code !== undefined) return code;
  }
  
  // Try progressive prefix matching for subspecies
  const parts = cleanName.split(" ");
  for (let i = parts.length - 1; i > 0; i--) {
    const prefix = parts.slice(0, i).join(" ");
    code = ABA_CODES[prefix];
    if (code !== undefined) return code;
  }
  
  return null;
}

export { ABA_CODES, ABA_MAX_CODE };
